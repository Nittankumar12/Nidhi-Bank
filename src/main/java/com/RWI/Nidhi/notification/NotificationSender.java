package com.RWI.Nidhi.notification;

public class NotificationSender {

    private String name;

    public NotificationSender() {
    }

    public NotificationSender(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
